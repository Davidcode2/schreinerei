const GENERATED_TYPES_PATH: &str = "frontend/src/types/generated.ts";
const HEADER: &str =
    "// This file was generated by [ts-rs](https://github.com/Aleph-Alpha/ts-rs). Do not edit this file manually.";

type Result<T> = std::result::Result<T, Box<dyn Error>>;

struct Binding {
    name: String,
    declaration: String,
}

fn main() {
    if let Err(error) = run() {
        eprintln!("failed to export TypeScript types: {error}");
        std::process::exit(1);
    }
}

fn run() -> Result<()> {
    let generated = generate_typescript()?;
    fs::write(GENERATED_TYPES_PATH, generated)?;
    println!("wrote {GENERATED_TYPES_PATH}");
    Ok(())
}

fn generate_typescript() -> Result<String> {
    let cfg = Config::new();
    let mut bindings = Vec::new();
    collect_bindings(&cfg, &mut bindings)?;
    bindings.sort_by(|left, right| left.name.cmp(&right.name));
    validate_unique_bindings(&bindings)?;

    let mut generated = String::from(HEADER);
    generated.push_str("\n\n");

    for (index, binding) in bindings.into_iter().enumerate() {
        if index > 0 {
            generated.push('\n');
        }

        generated.push_str(&binding.declaration);
        generated.push('\n');
    }

    Ok(generated)
}

